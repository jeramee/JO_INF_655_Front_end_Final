#!/bin/bash

# Set the paths
PROJECT_DIR="/var/www/html/iwant-food.com/public_html"
BACKUP_DIR="/var/www/html/iwant-food.com"

# Check if the backup directory exists
if [ ! -d "$BACKUP_DIR" ]; then
  echo "Backup directory does not exist!"
  exit 1
fi

# Check if the node_modules folder exists in the backup directory
if [ ! -d "$BACKUP_DIR/node_modules" ]; then
  echo "Node_modules folder not found in the backup directory!"
  exit 1
fi

# Check if the node_modules folder already exists in the project directory
if [ -d "$PROJECT_DIR/node_modules" ]; then
  echo "Node_modules folder already exists in the project directory!"
  exit 1
fi

# Copy the node_modules folder from the backup directory to the project directory
cp -r $BACKUP_DIR/node_modules $PROJECT_DIR

echo "Node_modules folder successfully recovered!"
